    partner_type TEXT,
    partner_id TEXT,
    partner_name_cache TEXT,
    ref_type TEXT,
    ref_id TEXT,
    description TEXT,
    evidence_url TEXT,
    status TEXT NOT NULL DEFAULT 'pending',
    cash_tally JSONB,
    ref_advance_id BIGINT,
    target_bank_info JSONB,
    bank_reference_id TEXT,
    book_type TEXT NOT NULL DEFAULT 'BOTH',
