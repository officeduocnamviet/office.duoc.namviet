CREATE SCHEMA IF NOT EXISTS public;

-- Sổ quỹ tiền mặt / tài khoản ngân hàng. id = bigint IDENTITY (prod sinh id).
-- balance = NUMERIC (số dư realtime, do trigger duy trì). KHÔNG có lock_version
-- (migrate-later, ADR 0002).
CREATE TABLE IF NOT EXISTS public.fund_accounts (
    id              bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name            text NOT NULL,
    type            text NOT NULL,
    account_number  text,
    bank_id         bigint,
    initial_balance numeric NOT NULL DEFAULT 0,
    balance         numeric NOT NULL DEFAULT 0,    -- số dư realtime (trigger duy trì)
    currency        text DEFAULT 'VND',
    status          text NOT NULL DEFAULT 'active',
    account_id      text,
    created_at      timestamptz DEFAULT now(),
    updated_at      timestamptz DEFAULT now(),
    deleted_at      timestamptz
);

-- Hàm cập nhật số dư quỹ — TÁI TẠO logic prod (handle_fund_balance_update). CHỈ
-- cộng/trừ balance khi phiếu sang status='completed' (INSERT thẳng completed hoặc
-- UPDATE pending→completed). Hoàn/xoá phiếu completed → đảo lại. pending/approved
-- KHÔNG đổi số dư. Đặt Ở ĐÂY (cùng file fund_accounts) vì hàm dùng fund_accounts;
-- TRIGGER gắn hàm này vào finance_transactions nằm ở public_orders.sql (chạy SAU,
-- khi bảng finance_transactions đã tạo). Mục đích test: KHẲNG ĐỊNH RecordPaymentIn
-- (Go) chỉ INSERT phiếu completed và KHÔNG tự update balance → trigger cộng ĐÚNG
-- MỘT LẦN (không double-count).
CREATE OR REPLACE FUNCTION public.handle_fund_balance_update() RETURNS trigger
    LANGUAGE plpgsql AS $$
BEGIN
    IF (TG_OP = 'UPDATE' AND NEW.status = 'completed' AND OLD.status <> 'completed')
       OR (TG_OP = 'INSERT' AND NEW.status = 'completed') THEN
        IF NEW.flow = 'in' THEN
            UPDATE public.fund_accounts SET balance = balance + NEW.amount, updated_at = now() WHERE id = NEW.fund_account_id;
        ELSIF NEW.flow = 'out' THEN
            UPDATE public.fund_accounts SET balance = balance - NEW.amount, updated_at = now() WHERE id = NEW.fund_account_id;
        END IF;
    ELSIF (TG_OP = 'UPDATE' AND NEW.status = 'cancelled' AND OLD.status = 'completed') THEN
        IF OLD.flow = 'in' THEN
            UPDATE public.fund_accounts SET balance = balance - OLD.amount WHERE id = OLD.fund_account_id;
        ELSIF OLD.flow = 'out' THEN
            UPDATE public.fund_accounts SET balance = balance + OLD.amount WHERE id = OLD.fund_account_id;
        END IF;
    ELSIF (TG_OP = 'DELETE' AND OLD.status = 'completed') THEN
        IF OLD.flow = 'in' THEN
            UPDATE public.fund_accounts SET balance = balance - OLD.amount WHERE id = OLD.fund_account_id;
        ELSIF OLD.flow = 'out' THEN
            UPDATE public.fund_accounts SET balance = balance + OLD.amount WHERE id = OLD.fund_account_id;
        END IF;
    END IF;
    RETURN NULL;
END;
$$;
