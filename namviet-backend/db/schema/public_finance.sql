--   AFTER INSERT/UPDATE/DELETE chạy handle_fund_balance_update() — TỰ cộng/trừ
--   fund_accounts.balance khi phiếu chuyển sang status='completed'. Do đó tầng Go
--   (RecordPaymentIn) chỉ INSERT phiếu với status='completed' và KHÔNG TỰ UPDATE
--   balance — nếu Go cũng cộng, số dư sẽ TĂNG GẤP ĐÔI. Trigger sở hữu số dư.
